// Object Literals

'use strict';

var manny = {
    firstName: 'Manny',
    lastName: 'Hagman',
    age: 35
};

console.log(manny);
console.log(manny.firstName);
console.log(manny.lastName);
console.log(manny.age);

//Object literals are used when you want to create a single Object and there wil be little to no reuse of the code later in the application.
